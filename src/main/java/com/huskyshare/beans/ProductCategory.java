package com.huskyshare.beans;

public class ProductCategory {
	private int id;
	private String name;
	private int level;
	private int pld;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the level
	 */
	public int getLevel() {
		return level;
	}
	/**
	 * @param level the level to set
	 */
	public void setLevel(int level) {
		this.level = level;
	}
	/**
	 * @return the pld
	 */
	public int getPld() {
		return pld;
	}
	/**
	 * @param pld the pld to set
	 */
	public void setPld(int pld) {
		this.pld = pld;
	}
}
